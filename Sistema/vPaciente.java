    private void btnBuscarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPActionPerformed
        conexion cc=new conexion();
        Connection cn = cc.conexion();
        lares.setVisible(true);
        fecha1.setVisible(true);
        String sql="";
        sql="SELECT * FROM paciente WHERE folio_p=?";
        try{
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,tfFolioP.getText());
            rs=pst.executeQuery();
            
            if(rs.next()){
                tfNombreP.setText(rs.getString("nombre"));
                tfApellidoPatP.setText(rs.getString("apellidoPat"));
                tfApellidoMatP.setText(rs.getString("apellidoMat"));
                String w = rs.getString("sexo");
                lares.setText(rs.getString("sexo"));
                if ( w.equalsIgnoreCase("F")){
                    femenino.setSelected(true);
                }else{ if(w.equalsIgnoreCase("M")){
                    masculino.setSelected(true);}
                    else{
                        System.out.println("Error");
                        }}
                fecha1.setText(rs.getString("fecha_nac"));
                FKdTablaPaciente.setText(rs.getString("clave_t"));
            }
            
        }catch (SQLException e) {
                //JOptionPane.showMessageDialog(null, String.valueOf(("Error: " + e)));
                JOptionPane.showMessageDialog(null, "ERROR");
        }
    }//GEN-LAST:event_btnBuscarPActionPerformed

